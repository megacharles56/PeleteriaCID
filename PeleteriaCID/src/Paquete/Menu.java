    private void btnAccederActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAccederActionPerformed
        String usuario=txtUsuario.getText();
        String pass=String.valueOf(pwdPass.getPassword());
        if(usuario.equals("gguerrero")&&pass.equals("elcid")||usuario.equals("diegocid")&&pass.equals("elcid")){
                String Nombre="";
                if(usuario.equals("gguerrero")){
                    Nombre="Gloria";
                }else if(usuario.equals("diegocid")){
                    Nombre="Diego";
                }
                Principal principal=new Principal(Nombre);
        principal.setVisible(true);
        principal.setLocationRelativeTo(null);
        this.setVisible(false);
        }else
            JOptionPane.showMessageDialog(this, "Usuario Incorrecto", "ERROR", JOptionPane.ERROR_MESSAGE);
    }//GEN-LAST:event_btnAccederActionPerformed

